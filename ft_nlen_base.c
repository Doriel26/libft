#include "libft.h"

int		ft_nlen_base(uintmax_t nbr, uintmax_t base)
{
	int i;

	i = 0;
	while (nbr >= base)
	{
		nbr = nbr / base;
		i++;
	}
	i++;
	return (i);
}
